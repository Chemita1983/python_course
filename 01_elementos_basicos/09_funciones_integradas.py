# Funciones integradas

numero = int("10")
print(numero)

numero = float("10")
print(numero)

cadena = str(10)
print(cadena)

binario = bin(10)
print(binario)

numero = int("0b1010", 2)
print(numero)

numero = int("0xa", 16)
print(numero)

numero = abs(-8)
print(numero)

# Redondea a lo que tenga mas cerca
numero = round(3.1416, 2)
print(numero)

texto = len("Chema")
print(texto)