
'''
Ejercicio 4

Hacer un programa para calcular el área y longitud de un círculo
Area = Pi * r^2
Longitud = 2 * Pi * r

'''

radio = float(input("Introduce el valor del radio: "))
area = 3.14 * radio ** 2
longitud = 2 * 3.14 * radio
print(f'El resultado de area es: {area}')
print(f'El resultado de longitud es: {longitud}')